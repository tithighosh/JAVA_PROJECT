    private void submitbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitbuttonActionPerformed
        // TODO add your handling code here:
        //radio button choice
        String radiochoice = "";
        if (jRadioButton1.isSelected()) {
            radiochoice = jRadioButton1.getText();
        }
        if (jRadioButton2.isSelected()) {
            radiochoice = jRadioButton2.getText();
        }
        //now insert into database
        if (valedatefields()) {
            ArrayList maidinfo = new ArrayList();
            maidinfo.add(nidnumberfield.getText());
            maidinfo.add(namefield.getText());
            maidinfo.add(passwordfield.getText());
            maidinfo.add(worktypefield.getText());
            maidinfo.add(locationfield.getText());
            maidinfo.add(holidayfield.getText());
            maidinfo.add(questioncombobox.getSelectedItem());
            maidinfo.add(answerfield.getText());
            maidinfo.add(radiochoice);
            maidinfo.add(salaryfield.getText());
            maidinfo.add(workinghourscombobox.getSelectedItem());
            maidinfo.add(phonenumberfield.getText());
            //database connect
            DBConnect dbc = new DBConnect();
            dbc.insertIntoDBformaid(maidinfo);

        }
        reset();


    }//GEN-LAST:event_submitbuttonActionPerformed

