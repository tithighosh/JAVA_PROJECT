    private boolean valedatefields() {
        if (nidnumberfield.getText().isEmpty()) {
            JOptionPane.showMessageDialog(MaidFrame.this, "Enter an Nid");
            nidnumberfield.grabFocus();
            return false;
        }
        if (namefield.getText().isEmpty()) {
            JOptionPane.showMessageDialog(MaidFrame.this, "Enter any Name");
            namefield.grabFocus();
            return false;
        }
        if (passwordfield.getText().isEmpty()) {
            JOptionPane.showMessageDialog(MaidFrame.this, "Enter password");
            passwordfield.grabFocus();
            return false;
        }
        if (worktypefield.getText().isEmpty()) {
            JOptionPane.showMessageDialog(MaidFrame.this, "Enter work type");
            worktypefield.grabFocus();
            return false;
        }
        if (locationfield.getText().isEmpty()) {
            JOptionPane.showMessageDialog(MaidFrame.this, "Enter location");
            locationfield.grabFocus();
            return false;
        }
        if (holidayfield.getText().isEmpty()) {
            JOptionPane.showMessageDialog(MaidFrame.this, "Enter holiday");
            holidayfield.grabFocus();
            return false;
        }
        if (answerfield.getText().isEmpty()) {
            JOptionPane.showMessageDialog(MaidFrame.this, "Select a question and give answer");
            return false;
        }
        if (!((jRadioButton1.isSelected()) || (jRadioButton2.isSelected()))) {
            JOptionPane.showMessageDialog(MaidFrame.this, "Enter Gender");
            return false;
        }
        if (salaryfield.getText().isEmpty()) {
            JOptionPane.showMessageDialog(MaidFrame.this, "Enter expected salary");
            return false;
        }
        if (phonenumberfield.getText().isEmpty()) {
            JOptionPane.showMessageDialog(MaidFrame.this, "Enter Phone number");
        }

        return true;
    }
