    private boolean valedatefields() {
        if (nidnumberfield.getText().isEmpty()) {
            JOptionPane.showMessageDialog(UserFrame.this, "Enter an Nid");
            nidnumberfield.grabFocus();
            return false;
        }
        if (namefield.getText().isEmpty()) {
            JOptionPane.showMessageDialog(UserFrame.this, "Enter any Name");
            namefield.grabFocus();
            return false;
        }
        if (passwordfield.getText().isEmpty()) {
            JOptionPane.showMessageDialog(UserFrame.this, "Enter password");
            passwordfield.grabFocus();
            return false;
        }
        if (emailfield.getText().isEmpty()) {
            JOptionPane.showMessageDialog(UserFrame.this, "Enter email");
            emailfield.grabFocus();
            return false;
        }
        if (addressfield.getText().isEmpty()) {
            JOptionPane.showMessageDialog(UserFrame.this, "Enter address");
            addressfield.grabFocus();
            return false;
        }
        if (answerfield.getText().isEmpty()) {
            JOptionPane.showMessageDialog(UserFrame.this, "Select a question and give answer");
            return false;
        }
        if (!((jRadioButton1.isSelected()) || (jRadioButton2.isSelected()))) {
            JOptionPane.showMessageDialog(UserFrame.this, "Enter Gender");
            return false;
        }
        if (phonenumberfield.getText().isEmpty()) {
            JOptionPane.showMessageDialog(UserFrame.this, "Enter Phone number");
        }

        return true;
    }
