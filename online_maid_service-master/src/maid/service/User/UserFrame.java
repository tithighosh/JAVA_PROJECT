    private void submitbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitbuttonActionPerformed
        // TODO add your handling code here:
        String radiochoice = "";
        if (jRadioButton1.isSelected()) {
            radiochoice = jRadioButton1.getText();
        }
        if (jRadioButton2.isSelected()) {
            radiochoice = jRadioButton2.getText();
        }
        //now insert into database
        if (valedatefields()) {
            ArrayList userinfo = new ArrayList();
            userinfo.add(nidnumberfield.getText());
            userinfo.add(namefield.getText());
            userinfo.add(passwordfield.getText());
            userinfo.add(emailfield.getText());
            userinfo.add(addressfield.getText());
            userinfo.add(companyfield.getText());
            userinfo.add(questioncombobox.getSelectedItem());
            userinfo.add(answerfield.getText());
            userinfo.add(radiochoice);
            userinfo.add(phonenumberfield.getText());
            DBConnect dbc = new DBConnect();
            dbc.insertIntoDBforuser(userinfo);
        }
        reset();

    }//GEN-LAST:event_submitbuttonActionPerformed

