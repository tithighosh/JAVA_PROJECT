    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        try {
            int row = jTable1.getSelectedRow();
            String re = jTable1.getValueAt(row, 0).toString();
            String sql = "DELETE from `contact form` where `Reason for contacting` =?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, re);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Deleted information");
            pst.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        refresh.doClick();
    }//GEN-LAST:event_deleteActionPerformed

    private void refreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        tableModel.setRowCount(0);
        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * from `contact form`");
            while (rs.next()) {
                String res =rs.getString("Reason for contacting");
                String name =rs.getString("Name");
                String email =rs.getString("Email");
                String num =rs.getString("Mobile Number");
                String city =rs.getString("City");
                String comment =rs.getString("Comment");
                tableModel.addRow(new Object[]{res,name,email,num,city,comment});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_refreshActionPerformed

