 private void populateJTableOnFormLoad() {
        // TODO add your handling code here:

        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        tableModel.setRowCount(0); //start inserting from first row
        tableModel.setColumnCount(6); //set column number

        //setting column headers
        jTable1.getColumnModel().getColumn(0).setHeaderValue("Reason for contacting");
        jTable1.getColumnModel().getColumn(1).setHeaderValue("Name");
        jTable1.getColumnModel().getColumn(2).setHeaderValue(" Email");
        jTable1.getColumnModel().getColumn(3).setHeaderValue("Mobile Number");
        jTable1.getColumnModel().getColumn(4).setHeaderValue("City");
        jTable1.getColumnModel().getColumn(5).setHeaderValue("Comment");
        jTable1.getTableHeader().repaint();

        try {
            DBConnect dbc = new DBConnect();
            ResultSet rs = dbc.getResultSetcontact();

            while (rs.next()) {
                String re = rs.getString("Reason for contacting");
                String name = rs.getString("Name");
                String email = rs.getString("Email");
                String city = rs.getString("City");
                String num = rs.getString("Mobile Number");
                String comment = rs.getString("Comment");

                tableModel.addRow(new Object[]{re, name, email,city, num, comment});
            }
        } catch (Exception e) {
            System.out.println("Error: " + e);
            e.printStackTrace();
        }
    }
